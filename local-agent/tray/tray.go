package tray

import (
	"fmt"
	"log"
	"os"

	"github.com/getlantern/systray"
	"github.com/skratchdot/open-golang/open"
)

var protocol string
var version string

// Init 初始化系统托盘
func Init(p, v string) error {
	protocol = p
	version = v

	// 在后台goroutine中运行systray
	go func() {
		systray.SetIcon(getIconData())
		systray.SetTitle("LocalHelper")
		systray.SetTooltip(fmt.Sprintf("本地文件助手 v%s", version))

		// 添加菜单项
		mShow := systray.AddMenuItem("📂 打开工作目录", "在工作目录中打开文件管理器")
		mOpen := systray.AddMenuItem("📄 打开文件", "手动选择并打开文件")
		mAbout := systray.AddMenuItem("ℹ️ 关于", "关于 LocalHelper")
		systray.AddSeparator()
		mQuit := systray.AddMenuItem("❌ 退出", "退出程序")

		log.Println("系统托盘初始化完成")

		// 监听菜单点击
		for {
			select {
			case <-mShow.ClickedCh:
				// 打开用户文档目录
				homeDir, _ := os.UserHomeDir()
				open.Start(homeDir)
			case <-mOpen.ClickedCh:
				// 打开文件管理器
				open.Start("explorer.exe")
			case <-mAbout.ClickedCh:
				log.Printf("LocalHelper v%s - 本地文件助手", version)
			case <-mQuit.ClickedCh:
				log.Println("用户请求退出")
				systray.Quit()
				os.Exit(0)
			}
		}
	}()

	return nil
}

